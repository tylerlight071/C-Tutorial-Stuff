#include <iostream>
// this adds more maths functions to use
#include <cmath>

using namespace std;

int main()
{
    // floor will round down a value (the number in () ) to the closest whole number
    double floor_function = floor(1.2);

    cout << floor_function;

    // pow will raise a number to the closest power, in this case, it will be 2 to the power of 3
    double pow_function = pow(2, 3);

    cout << pow_function;

    return 0;
}

