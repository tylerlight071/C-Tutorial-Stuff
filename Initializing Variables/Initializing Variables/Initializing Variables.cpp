#include <iostream>

using namespace std;

int main() {

	// stores variable as a decimal
	double price = 99.99;

	// stores variable as a decimal but not as precise (you can use f or F)
	float interestRate = 3.67f;

	// used to store larger variables (can use l or L but best to use L)
	long fileSize = 90000L;

	// used to represent individual characters --> includes punctuation, letters, numbers and symbols. use ' ' as it is a single charater
	char letter = 'a';

	// used to determine if something is true or false 
	bool isValid = false;

	// auto changes the type to what it is meant to be automatically
	auto changeMyValue = false;

	return 0;
}